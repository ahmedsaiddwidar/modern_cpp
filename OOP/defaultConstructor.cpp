/*
- A default constructor is a constructor with no parameters, or all parameters have default values.
- It automatically initializes an object without requiring any arguments.
- If you don't define any constructor, the compiler generates a default one automatically.
- If you define any constructor (with parameters) and don't define a default constructor manually, 
then you lose the compiler-generated default constructor.
*/
